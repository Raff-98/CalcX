class Calculadora   //São os calculos que a calculadora será capaz de fazer
{
    Somar(num1, num2) 
    {
        return num1 + num2;
    }

    Subtrair(num1, num2) 
    {
        return num1 - num2;
    }

    Dividir(num1, num2) 
    {
        return num1 / num2;
    }

    Multiplicar(num1, num2) 
    {
        return num1 * num2;
    }
} 